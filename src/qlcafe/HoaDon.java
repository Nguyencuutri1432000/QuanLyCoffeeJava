    private void tblHDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHDMouseClicked
        // TODO add your handling code here:
         int rowSelected = this.tblHD.getSelectedRow();
         txtCTHD.setText((String) tblHD.getValueAt(rowSelected, 0));
        txtTen.setText((String) tblHD.getValueAt(rowSelected, 1));
        txtNgay.setText((String) tblHD.getValueAt(rowSelected, 2));
        txtTong.setText((String) tblHD.getValueAt(rowSelected, 3));
        try {
            String sql = "SELECT * FROM CHITIETHOADON where MACTHD = '"+txtCTHD.getText()+"'";          
            result = statement.executeQuery(sql);
            dtmCT.setRowCount(0);
            while (result.next()) {
                String macthd = result.getString("MACTHD");
                String tensp = result.getString("TENSANPHAM");
                String sl = result.getString("SOLUONG");
                String giamgia = result.getString("GIAMGIA");
                String dongia = result.getString("DONGIA");
                Vector<Object> vec = new Vector<Object>();
                vec.add(macthd);                
                vec.add(tensp);
                vec.add(sl);
                vec.add(giamgia);
                vec.add(dongia);
                dtmCT.addRow(vec);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_tblHDMouseClicked

