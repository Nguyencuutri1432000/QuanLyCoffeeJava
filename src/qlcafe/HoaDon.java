    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (tblHD.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "vui lòng chọn trước khi xóa");
            return;
        }
        int ques = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa không",
            "xóa danh sách?", JOptionPane.YES_NO_OPTION);
        if (ques == JOptionPane.NO_OPTION) {
            return;
        } else {
            try {
                String sqlXoa = "DELETE FROM HOADON WHERE MACTHD = '" + txtCTHD.getText() + "'";
                int x = statement.executeUpdate(sqlXoa);
                if (x > 0) {
                    hienThiDSHD();
                           txtCTHD.setText("");
        txtTen.setText("");
        txtNgay.setText("");
        txtTong.setText("");
        txtCTHD.requestFocus();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

