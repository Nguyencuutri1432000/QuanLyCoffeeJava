    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (kiemTraMaSoTonTai(txtCTHD.getText()) == true) {
            //khách hàng này đã tồn tại
            int tl = JOptionPane.showConfirmDialog(null,
                "Mã đã tồn tại, bạn có muốn cập nhật?",
                "Trùng Mã số", JOptionPane.YES_NO_OPTION);
            if (tl == JOptionPane.NO_OPTION) {
                return;
            } else {
                //trường họp usre muốn cập nhật (update) họ tên, năm sinh
                try {
                    String sqlUpdate = "UPDATE HOADON SET TENKH = N'"
                    + txtTen.getText() + "', NGAYBAN= '" + txtNgay.getText() + "',TONGTIEN= " + txtTong.getText() + " WHERE MACTHD = '"
                    + txtCTHD.getText() + "'";
                    int x = statement.executeUpdate(sqlUpdate);
                    if (x > 0) {
                        hienThiDSHD();
                    }
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        } else 
        {
            try {
                String sqlInsert = " INSERT INTO HOADON(MACTHD,TENKH,NGAYBAN)  VALUES(" + txtCTHD.getText() + ",N'"
                + txtTen.getText() + "', '" + txtNgay.getText() + "')";
                int x = statement.executeUpdate(sqlInsert);
                if (x > 0) {
                    hienThiDSHD();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

