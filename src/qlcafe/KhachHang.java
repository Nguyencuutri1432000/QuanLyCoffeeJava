    private void tblKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKHMouseClicked
        // TODO add your handling code here:
        int rowSelected = this.tblKH.getSelectedRow();
        txtMaKH.setText((String) tblKH.getValueAt(rowSelected, 0));
        txtTenKH.setText((String) tblKH.getValueAt(rowSelected, 1));
        txtSDT.setText((String) tblKH.getValueAt(rowSelected, 2));
        txtDiaChi.setText((String) tblKH.getValueAt(rowSelected, 3));
        txtGT.setText((String) tblKH.getValueAt(rowSelected, 4));
    }//GEN-LAST:event_tblKHMouseClicked

