     private void hienThiDSKH() {
        try {
            String sql = "SELECT * FROM KHACHHANG";
            
            result = statement.executeQuery(sql);
            dtmKH.setRowCount(0);
            while (result.next()) {
                String ma = result.getString("MAKH");
                String tennv = result.getString("HOTEN");
                String sdt = result.getString("SDT");
                String diachi = result.getString("DIACHI");
                String gioitinh = result.getString("GIOITINH");
                Vector<Object> vec = new Vector<Object>();
                vec.add(ma);
                vec.add(tennv);
                vec.add(sdt);
                vec.add(diachi);
                vec.add(gioitinh);
                dtmKH.addRow(vec);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
     
     public boolean kiemTraMaSoTonTai(String maso) {
        try {
            String sqlSelect = "SELECT * FROM KHACHHANG WHERE MAKH = '" + maso + "'";
            result = statement.executeQuery(sqlSelect);
            if (result.next()) {
                return true;
            } else {
                return false;
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return false;
    }
     
